"use client";

import { Inter } from "next/font/google";
import StyledComponentsRegistry from "@/library/AntdRegistry";
import "@/app/globals.css";
import { AuthContext, AuthProvider } from "@/context/AuthContext";
import { useContext, useEffect } from "react";
import { useRouter, usePathname } from "next/navigation";
import { ShowCommentProvider } from "@/context/ShowCommentContext";
import {
  GlobalContextProvider,
  useGlobalContext,
} from "@/library/global.context";
import { roleRoutes, defaultRoutes } from "@/utils/roleRoute";
import CircularProgress from "@/components/music/progress";
import Image from "next/image";

const inter = Inter({ subsets: ["latin"] });

function AuthWrapper({ children }: { children: React.ReactNode }) {
  const { user } = useContext(AuthContext) ?? {};
  const router = useRouter();
  const pathname = usePathname();
  const { progressUploadMusic, informationUpload } = useGlobalContext()!;
  useEffect(() => {
    if (typeof window !== "undefined") {
      const userRole = user?.role ?? "GUEST";
      const allowedRoutes = roleRoutes[userRole] || [];

      const isAllowed = allowedRoutes.some((route) =>
        route instanceof RegExp ? route.test(pathname) : route === pathname
      );

      if (!isAllowed) {
        const timeout = setTimeout(() => {
          if (
            !allowedRoutes.some((route) =>
              route instanceof RegExp
                ? route.test(pathname)
                : route === pathname
            )
          ) {
            router.replace(defaultRoutes[userRole] || "/page/trending-guest");
          }
        }, 500);

        return () => clearTimeout(timeout);
      }
    }
  }, [user, pathname]);

  return (
 <div>
      {progressUploadMusic !== 0 && <div className="absolute gap-4 top-2 left-28 z-30 flex items-center justify-start bg-white border border-zinc-200 rounded-lg shadow-lg w-fit h-20 p-4">
        <CircularProgress progress={progressUploadMusic} size={50} strokeWidth={3} />
        <img
          alt="image"
          style={{
            width: 50,
            height: 50,
            borderRadius: 5
          }}
          src={informationUpload?.image}
        />
        <h5 className="text-1xl font-medium">{informationUpload?.name}</h5>
      </div>}

      {children}
    </div>
  );
}

export default function RootLayout({
  children,
}: Readonly<{ children: React.ReactNode }>) {
  return (
    <GlobalContextProvider>
      <AuthProvider>
        <ShowCommentProvider>
          <html lang="en">
            <head>
              <title>Stiktify</title>
              <meta name="description" content="Generated by create next app" />
              <link rel="icon" href="/favicon.ico" />
            </head>
            <body className={inter.className}>
              <StyledComponentsRegistry>
                <AuthWrapper>{children}</AuthWrapper>
              </StyledComponentsRegistry>
            </body>
          </html>
        </ShowCommentProvider>
      </AuthProvider>
    </GlobalContextProvider>
  );
}
